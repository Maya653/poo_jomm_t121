altura = float(input("Ingresa la altura del dinosaurio en metros: ")) # solicite la altura
longitud = altura * 6 # Calcula la longitud del dinosaurio
peso = altura ** 2 * 100 # Calcula el peso del dinosaurio
# Mostramos los resultados al usuario
print("La longitud del dinosaurio es de", longitud, "metros.") #imprima la longitud 
print("El peso del dinosaurio es de", peso, "kilogramos.") #impirma el peso